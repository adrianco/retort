defmodule BrazilianSoccerMcp do
  @moduledoc """
  Brazilian Soccer MCP Server.

  Provides MCP (Model Context Protocol) tools for querying Brazilian soccer data:
  - find_matches: Search match results across Brasileirao, Copa do Brasil, Libertadores
  - get_team_stats: Win/draw/loss records and goal statistics for any team
  - find_players: Search FIFA player database by name, nationality, or club
  - get_competition_standings: Calculated league tables from match results
  - get_statistics: Aggregated analytics (biggest wins, goals per match, home/away records)
  """
end
